class Address {
    constructor(ownerID, email, phone, street, postalCode, city, country, geolocation) {
        this.ownerID = ownerID;
        this.email = email;
        this.phone = phone;
        this.street = street;
        this.postalCode = postalCode;
        this.city = city;
        this.country = country;
        this.geolocation = geolocation; 
    }

}